            pstmt.setInt(2,Integer.parseInt(b_id));
            rs2 = pstmt.executeQuery();

            while(rs2.next())
            {
                String userId = rs2.getString("user_id");
                String u_name = rs2.getString("user_name");
                String address = rs2.getString("address");
                String age = rs2.getString("age");
                String phone = rs2.getString("phone");
                String email = rs2.getString("email");
                String branch = rs2.getString("branch_id");
                
                String b_name = sql.get_branch(branch);
                String balence = sql.get_balance(u_id);
                //fill_level();
                jTextField18.setText(userId);
                jTextField19.setText(u_name);
                jTextField20.setText(address);
                jTextField21.setText(phone);
                jTextField22.setText(email);
                jTextField28.setText(age);
                jTextField29.setText(b_name);
                jTextField30.setText(balence);
            } 

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:  Delete user account 
        String us_id = jTextField2.getText();
        if(us_id.equals("")){
            JOptionPane.showMessageDialog(this, "UserID Required !!");
        }else{
            String balence = sql.get_balance(us_id);
            boolean loan = sql.previous_loan(us_id);
            double balance = Double.parseDouble(balence);
            if(sql.verifie_user_id(us_id, b_id) && balance == 0 && !loan)
            {
                if(sql.delet_user(us_id, b_id)){
                    JOptionPane.showMessageDialog(this, "Deletion Successful..");
                }else{
                    JOptionPane.showMessageDialog(this, "Invalied User ID !!");
                }
            }else{
                JOptionPane.showMessageDialog(this, "User is not in this branch or Account Book not clear !!");
            }
        }
        
    }//GEN-LAST:event_jButton11ActionPerformed

